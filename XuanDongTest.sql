);


--TAO BANG GIA_LAN_BANH
CREATE TABLE GIA_LAN_BANH(
    MA_TINH NUMBER(2) PRIMARY KEY CHECK (MA_TINH >0 AND MA_TINH <=64),
    TEN_TINH VARCHAR(50) NOT NULL,
    PHI_TRUOC_BA NUMBER(5,3) DEFAULT 0.10 NOT NULL CHECK (PHI_TRUOC_BA >0 AND PHI_TRUOC_BA <1),
    PHI_SD_DUONG_BO NUMBER(15) NOT NULL CHECK(PHI_SD_DUONG_BO >0),
    BAO_HIEM_TNDS NUMBER(15) NOT NULL CHECK(BAO_HIEM_TNDS >0),
    PHI_DK_BIEN_SO NUMBER(15) NOT NULL CHECK(PHI_DK_BIEN_SO >0),
    PHI_DANG_KIEM NUMBER(10) NOT NULL CHECK(PHI_DANG_KIEM >0),
    CONSTRAINT CHECK_TEN_TINH UNIQUE (TEN_TINH)
);

