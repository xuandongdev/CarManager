    MA_XE_LB    XE.MA_XE%TYPE,
    TP          GIA_LAN_BANH.MA_TINH%TYPE
)   RETURN NUMBER
IS  
    TONG        NUMBER;
    GIA_NY      XE.GIA_NIEM_YET%TYPE;
    PHI_TB      GIA_LAN_BANH.PHI_TRUOC_BA%TYPE;
    PHI_SDDB    GIA_LAN_BANH.PHI_SD_DUONG_BO%TYPE;
    BAO_HIEM    GIA_LAN_BANH.BAO_HIEM_TNDS%TYPE;
    PHI_BIEN    GIA_LAN_BANH.PHI_DK_BIEN_SO%TYPE;
    PHI_DKIEM   GIA_LAN_BANH.PHI_DANG_KIEM%TYPE;
BEGIN
    TONG := 0;
    SELECT GIA_NIEM_YET INTO GIA_NY
    FROM XE
    WHERE MA_XE = MA_XE_LB;
    SELECT PHI_TRUOC_BA, PHI_SD_DUONG_BO, BAO_HIEM_TNDS, PHI_DK_BIEN_SO, PHI_DANG_KIEM
    INTO PHI_TB, PHI_SDDB, BAO_HIEM, PHI_BIEN, PHI_DKIEM
    FROM GIA_LAN_BANH
    WHERE MA_TINH = TP;
    
    TONG := GIA_NY + (GIA_NY * PHI_TB) + PHI_SDDB + BAO_HIEM + PHI_BIEN + PHI_DKIEM;
    RETURN TONG;
END;

