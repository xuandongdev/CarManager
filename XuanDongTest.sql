CREATE TABLE DATA_LOG_DELETE(
    ID          NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    MA_XE       CHAR(5),
    MODIFIED_BY VARCHAR2(50)
);

--TAO TRIGGER CHECK DELETE LOG
CREATE OR REPLACE PROCEDURE XOA_XE(
    p_ma_xe CHAR,
    p_modified_by VARCHAR2 DEFAULT NULL
) AS
BEGIN
    -- Xóa xe từ bảng XE
    DELETE FROM XE WHERE MA_XE = p_ma_xe;

    -- Ghi log xóa vào bảng DATA_LOG_DELETE, sử dụng `USER` nếu `p_modified_by` là NULL
    INSERT INTO DATA_LOG_DELETE (
        MA_XE,
        MODIFIED_BY
    ) VALUES (
        p_ma_xe,
        NVL(p_modified_by, USER) -- Dùng `USER` nếu `p_modified_by` là NULL
    );

    COMMIT;
END;

