    MA_HANG_XE CHAR(5) PRIMARY KEY,
    TEN_HANG_XE VARCHAR2(50)
);
--TAO BANG XE
CREATE TABLE XE(
    MA_XE CHAR(5) PRIMARY KEY,
    MA_HANG_XE CHAR(5) NOT NULL,
    DONG_XE VARCHAR2(50) NOT NULL,
    PHIEN_BAN VARCHAR2(50) NOT NULL,
    PHAN_KHUC VARCHAR2(50) NOT NULL,
    DONG_CO VARCHAR2(50) NOT NULL,
    GIA_NIEM_YET NUMBER(20) NOT NULL,
    DAM_PHAN VARCHAR2(150),
